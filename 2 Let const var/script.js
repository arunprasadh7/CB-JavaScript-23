// there are 3 ways to declare variables in JS - let, var, const

// let values can be updated in the future
let age;
age = 10;
age = 20;

console.log(age)

// const - values cannot be changed or updated 
const birthMonth = 'August';

console.log(birthMonth)

// var - similar to let but older version
var myName = 'Arun';
myName = 'John Wick';

console.log(myName)

/* const is immutable
  var is mutable - older version / but they are function scoped
  let is also mutable - newer version
  */

